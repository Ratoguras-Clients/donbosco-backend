    public static function getLinkClasses($isActive, $colorClasses)
    {
        if ($isActive) {
            return $colorClasses['active'];
        }
        return $colorClasses['base_text'] . ' ' . $colorClasses['hover'];
    }

    public static function isDarkColor($hex)
    {
        // Remove # if present
        $hex = ltrim($hex, '#');

        // Convert to RGB
        $r = hexdec(substr($hex, 0, 2));
        $g = hexdec(substr($hex, 2, 2));
        $b = hexdec(substr($hex, 4, 2));

        // Calculate brightness (YIQ formula)
        $brightness = (($r * 299) + ($g * 587) + ($b * 114)) / 1000;

        return $brightness < 128;
    }
